package com.github.yangwk.more.demo.javase.swing.atm.dao;

public class BankCard {
	private String cardId;
	private String cardPassword;
	private double cardMoney;
	private String cardName;
	private String cardMasterName;
	
	public String getCardId() {
		return cardId;
	}
	public void setCardId(String cardId) {
		this.cardId = cardId;
	}
	public String getCardPassword() {
		return cardPassword;
	}
	public void setCardPassword(String cardPassword) {
		this.cardPassword = cardPassword;
	}
	public double getCardMoney() {
		return cardMoney;
	}
	public void setCardMoney(double cardMoney) {
		this.cardMoney = cardMoney;
	}
	public String getCardName() {
		return cardName;
	}
	public void setCardName(String cardName) {
		this.cardName = cardName;
	}
	public String getCardMasterName() {
		return cardMasterName;
	}
	public void setCardMasterName(String cardMasterName) {
		this.cardMasterName = cardMasterName;
	}
	
}
